/**
 * OA SVG Export Module
 * Generates SVG cutting templates from OA card models.
 * 
 * Line conventions:
 *   - Solid (black):      Cut lines (face outlines + card boundary)
 *   - Dashed (red):       Mountain fold lines
 *   - Dot-dash (blue):    Valley fold lines
 *   - Light gray dashed:  Card center fold line
 */
OA.SvgExport = (function() {

  // SVG namespace
  var NS = "http://www.w3.org/2000/svg";

  // Default export settings
  var defaults = {
    // Scale factor: OA units to mm (1 unit = 1mm by default)
    scale: 1,
    // SVG stroke widths (in mm)
    cutStrokeWidth: 0.5,
    foldStrokeWidth: 0.35,
    // Colors
    cutColor: "#000000",
    mountainColor: "#DD3333",
    valleyColor: "#3366CC",
    centerFoldColor: "#999999",
    // Dash patterns (in mm)
    mountainDash: "3,2",
    valleyDash: "4,1.5,1,1.5",
    centerFoldDash: "6,3",
    // Padding around card (mm)
    padding: 10,
    // Include registration marks
    showRegistration: false
  };

  /**
   * Generate SVG string from the current OA model state.
   * 
   * @param {OA.Model} oaModel - The OA model instance
   * @param {Object} opts - Override options (merged with defaults)
   * @returns {string} Complete SVG document as a string
   */
  function generateSVG(oaModel, opts) {
    var settings = $.extend({}, defaults, opts);
    var s = settings.scale;

    // Get model data
    var modelData = oaModel.getModel();
    var cardW = modelData.settings.cardW;
    var cardH = modelData.settings.cardH;

    // Get the 2D-flattened clipped faces (at 180°)
    var clippedFaces = oaModel.getCloneClippedFaces();

    // Get fold lines
    var foldLines = oaModel.foldLines;

    // The 2D pattern coordinate system:
    //   X: 0 to cardW (left to right)
    //   Y: -cardH to cardH (bottom half to top half, fold at Y=0)
    //
    // For SVG we need to flip Y (SVG Y goes downward).
    // We'll map:
    //   OA X -> SVG x (with padding offset)
    //   OA Y -> SVG y = padding + cardH - OA_Y (so Y=cardH maps to top, Y=-cardH maps to bottom)

    var svgW = (cardW * s) + (settings.padding * 2);
    var svgH = (cardH * 2 * s) + (settings.padding * 2);
    var pad = settings.padding;

    // Coordinate transform functions
    function tx(x) { return (x * s) + pad; }
    function ty(y) { return pad + (cardH * s) - (y * s); }

    // Start building SVG
    var svgParts = [];

    // SVG header with mm units
    svgParts.push(
      '<?xml version="1.0" encoding="UTF-8" standalone="no"?>',
      '<svg xmlns="http://www.w3.org/2000/svg"',
      '  width="' + svgW + 'mm" height="' + svgH + 'mm"',
      '  viewBox="0 0 ' + svgW + ' ' + svgH + '"',
      '  version="1.1">',
      '',
      '<!-- Generated by webOAcard SVG Export -->',
      '<!-- Card dimensions: ' + cardW + ' x ' + (cardH * 2) + ' (W x H) -->',
      '',
      '<defs>',
      '  <style type="text/css">',
      '    .cut-line { fill:none; stroke:' + settings.cutColor + '; stroke-width:' + settings.cutStrokeWidth + '; stroke-linecap:round; stroke-linejoin:round; }',
      '    .mountain-fold { fill:none; stroke:' + settings.mountainColor + '; stroke-width:' + settings.foldStrokeWidth + '; stroke-dasharray:' + settings.mountainDash + '; }',
      '    .valley-fold { fill:none; stroke:' + settings.valleyColor + '; stroke-width:' + settings.foldStrokeWidth + '; stroke-dasharray:' + settings.valleyDash + '; }',
      '    .center-fold { fill:none; stroke:' + settings.centerFoldColor + '; stroke-width:' + settings.foldStrokeWidth + '; stroke-dasharray:' + settings.centerFoldDash + '; }',
      '  </style>',
      '</defs>',
      ''
    );

    // Layer: Card boundary (cut line)
    svgParts.push('<g id="card-boundary" inkscape:label="Card Boundary" inkscape:groupmode="layer" xmlns:inkscape="http://www.inkscape.org/namespaces/inkscape">');
    svgParts.push(
      '  <rect class="cut-line" x="' + tx(0) + '" y="' + ty(cardH) + '"',
      '    width="' + (cardW * s) + '" height="' + (cardH * 2 * s) + '" />'
    );
    svgParts.push('</g>');
    svgParts.push('');

    // Layer: Center fold line
    svgParts.push('<g id="center-fold" inkscape:label="Center Fold" inkscape:groupmode="layer" xmlns:inkscape="http://www.inkscape.org/namespaces/inkscape">');
    svgParts.push(
      '  <line class="center-fold"',
      '    x1="' + tx(0) + '" y1="' + ty(0) + '"',
      '    x2="' + tx(cardW) + '" y2="' + ty(0) + '" />'
    );
    svgParts.push('</g>');
    svgParts.push('');

    // Layer: Cut lines (face outlines)
    svgParts.push('<g id="cut-lines" inkscape:label="Cut Lines" inkscape:groupmode="layer" xmlns:inkscape="http://www.inkscape.org/namespaces/inkscape">');

    // Extract outlines from clipped faces
    if (clippedFaces && clippedFaces.length) {
      $.each(clippedFaces, function(i, face) {
        // Skip the base faces (they're the card background)
        if (face.oaInfo && face.oaInfo.name &&
            (face.oaInfo.name === "baseVFace" || face.oaInfo.name === "baseHFace")) {
          return;
        }

        var contours = face.getExPolygons();
        if (!contours) return;

        $.each(contours, function(j, exPoly) {
          // Outer contour
          if (exPoly.outer && exPoly.outer.length > 1) {
            var pathData = buildPathFromPoints(exPoly.outer, tx, ty, true);
            svgParts.push('  <path class="cut-line" d="' + pathData + '" />');
          }

          // Holes (also cut lines)
          if (exPoly.holes) {
            $.each(exPoly.holes, function(k, hole) {
              if (hole && hole.length > 1) {
                var holePath = buildPathFromPoints(hole, tx, ty, true);
                svgParts.push('  <path class="cut-line" d="' + holePath + '" />');
              }
            });
          }
        });
      });
    }

    svgParts.push('</g>');
    svgParts.push('');

    // Layer: Mountain fold lines
    svgParts.push('<g id="mountain-folds" inkscape:label="Mountain Folds" inkscape:groupmode="layer" xmlns:inkscape="http://www.inkscape.org/namespaces/inkscape">');

    if (foldLines && foldLines.mountain) {
      $.each(foldLines.mountain, function(i, ln) {
        if (ln && ln.length === 2) {
          svgParts.push(
            '  <line class="mountain-fold"',
            '    x1="' + tx(ln[0].X) + '" y1="' + ty(ln[0].Y) + '"',
            '    x2="' + tx(ln[1].X) + '" y2="' + ty(ln[1].Y) + '" />'
          );
        }
      });
    }

    svgParts.push('</g>');
    svgParts.push('');

    // Layer: Valley fold lines
    svgParts.push('<g id="valley-folds" inkscape:label="Valley Folds" inkscape:groupmode="layer" xmlns:inkscape="http://www.inkscape.org/namespaces/inkscape">');

    if (foldLines && foldLines.valley) {
      $.each(foldLines.valley, function(i, ln) {
        if (ln && ln.length === 2) {
          svgParts.push(
            '  <line class="valley-fold"',
            '    x1="' + tx(ln[0].X) + '" y1="' + ty(ln[0].Y) + '"',
            '    x2="' + tx(ln[1].X) + '" y2="' + ty(ln[1].Y) + '" />'
          );
        }
      });
    }

    svgParts.push('</g>');
    svgParts.push('');

    // Close SVG
    svgParts.push('</svg>');

    return svgParts.join('\n');
  }

  /**
   * Build an SVG path data string from an array of {X, Y} points.
   * 
   * @param {Array} points - Array of {X, Y} objects
   * @param {Function} tx - X coordinate transform
   * @param {Function} ty - Y coordinate transform
   * @param {boolean} closePath - Whether to close the path with Z
   * @returns {string} SVG path data string
   */
  function buildPathFromPoints(points, tx, ty, closePath) {
    var d = [];
    $.each(points, function(i, pt) {
      var cmd = (i === 0) ? 'M' : 'L';
      d.push(cmd + round(tx(pt.X)) + ',' + round(ty(pt.Y)));
    });
    if (closePath) {
      d.push('Z');
    }
    return d.join(' ');
  }

  /**
   * Round to 3 decimal places for clean SVG output.
   */
  function round(val) {
    return Math.round(val * 1000) / 1000;
  }

  /**
   * Trigger a download of the SVG file.
   * 
   * @param {string} svgString - The SVG content
   * @param {string} filename - Desired filename
   */
  function downloadSVG(svgString, filename) {
    filename = filename || 'oaCard_template.svg';
    var blob = new Blob([svgString], { type: 'image/svg+xml;charset=utf-8' });
    var url = (window.URL || window.webkitURL).createObjectURL(blob);
    var link = document.createElement('a');
    link.setAttribute('href', url);
    link.setAttribute('download', filename);
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    setTimeout(function() {
      (window.URL || window.webkitURL).revokeObjectURL(url);
    }, 250);
  }

  /**
   * Main export function: generate SVG and trigger download.
   * 
   * @param {OA.Model} oaModel - The OA model instance
   * @param {Object} opts - Optional settings overrides
   */
  function exportSVG(oaModel, opts) {
    var svg = generateSVG(oaModel, opts);
    var date = new Date();
    var dateStr = date.getFullYear() + '-' +
      ('0' + (date.getMonth() + 1)).slice(-2) + '-' +
      ('0' + date.getDate()).slice(-2);
    downloadSVG(svg, 'oaCard_' + dateStr + '.svg');
  }

  // Public API
  return {
    generateSVG: generateSVG,
    downloadSVG: downloadSVG,
    exportSVG: exportSVG
  };

})();
